    void listar() {
        String sql = "Select * from PRODUCTO";
        CConexion con = new CConexion();
        Connection cn;
        Statement st;
        ResultSet rs;
        DefaultTableModel modelo;
        
        try {
            /* ejemplo de sql:
            insert into TIENDA.PRODUCTO(idproducto,descripcion,pvp,coste,fecha) 
            values ('023p32','Gatera','18.7','15.25','2022-12-31');
            
            videos:
            https://www.youtube.com/watch?v=Erw8W5pH_T4
            https://www.youtube.com/watch?v=Fxk77AJPejM
            https://www.youtube.com/watch?v=8DucQvDocN8
            https://www.youtube.com/watch?v=ls-iubz0zE4
             */
            cn = con.estableceConexión();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            modelo = (DefaultTableModel) Tabladatos.getModel();
            modelo.setRowCount(0); /* borro resultados anteriores */
            Object[] fila = new Object[6];
            while (rs.next()) {
                fila[0] = rs.getInt("id"); /* valor automatico que lo pone automaticamente */
                fila[1] = rs.getString("idproducto");
                fila[2] = rs.getString("descripcion");
                fila[3] = rs.getFloat("pvp");
                fila[4] = rs.getFloat("coste");
                fila[5] = rs.getString("fecha");
                modelo.addRow(fila);
            }
            Tabladatos.setModel(modelo);
        } catch (Exception e) {
        }
        
    }


